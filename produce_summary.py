def delivery_report(filename):
    the_file = open(filename)
    for line in the_file:
        line = line.rstrip()
        words = line.split('|')

        melon = words[0]
        count = words[1]
        amount = words[2]

        print(f"Delivered {count} {melon}s for total of ${amount}")
    the_file.close()
    print("\n")




print("Day 1")
delivery_report("um-deliveries-20140519.txt")


